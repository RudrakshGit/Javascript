//Loop through arrays
const items = [
    'book', 'table', 'chair'
];

for (const item of items){
    console.log(item);
}

//Loop through objects
const users = [{name:'Mukesh'},{name:'Suresh'},{name:'Ramesh'}];

for (const user of users){
    console.log(user.name);
}

//Loop through strings
const str = "Hello World!";

for (const letter of str){
    console.log(letter);
}
